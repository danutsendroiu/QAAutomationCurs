package JavaBasic1;

import java.io.File;

public class Ex5 {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int multiply(int a, int b) {
        return a * b;
    }

    public static int substract(int a, int b) {
        return a - b;
    }

    public static double divide(int a, int b) {
        return (double) a / b;
    }

    public static int reminder(int a, int b) {
        return a % b;
    }

}
